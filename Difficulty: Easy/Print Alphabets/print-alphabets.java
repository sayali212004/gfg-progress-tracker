// function should print the alphabets
// from c1 to c2 (space separated)
// No need to print the new line
// The new line will be printed by the
// driver's code.
class Solution {
    public static void alphabets(char c1, char c2) {
        // code here
        while(c1<=c2)
        {
            System.out.print(c1+" ");
            c1++;
        }
        
    }
}